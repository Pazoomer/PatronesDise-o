
package gui;

/**
 *
 * @author t1pas
 */
public class Usuario {
    public String username;
    public String GmailPassword;
    public String OutlookPassword;
    public String SengridPassword;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getGmailPassword() {
        return GmailPassword;
    }

    public void setGmailPassword(String GmailPassword) {
        this.GmailPassword = GmailPassword;
    }

    public String getOutlookPassword() {
        return OutlookPassword;
    }

    public void setOutlookPassword(String OutlookPassword) {
        this.OutlookPassword = OutlookPassword;
    }

    public String getSengridPassword() {
        return SengridPassword;
    }

    public void setSengridPassword(String SengridPassword) {
        this.SengridPassword = SengridPassword;
    }
    
    
}
